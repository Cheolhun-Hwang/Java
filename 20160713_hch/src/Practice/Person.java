package Practice;

public class Person {
	private String name;
	private String birth;
	private String height;
	
	public Person(){
	}
	
	public String getName(){
		return name;
	}
	
	public String getBirth(){
		return birth;
	}
	
	public String getHeight(){
		return height;
	}
	
	public void setName(String a){
		this.name = a;
	}
	public void setBirth(String a){
		this.birth = a;
	}
	public void setHeight(String a){
		this.height = a;
	}
	

}
